// Firebase connection has been replaced with local storage to allow for 
// standalone deployment without specialized authorized domains.

export enum OperationType {
  CREATE = 'create',
  UPDATE = 'update',
  DELETE = 'delete',
  LIST = 'list',
  GET = 'get',
  WRITE = 'write',
}

export function handleFirestoreError(error: unknown, operationType: OperationType, path: string | null) {
  console.error('Storage Error: ', { error, operationType, path });
}
